export type I18nKey = keyof typeof zh;

export const editorMessages: Record<EditorLanguage, Record<I18nKey, string>> = {
    'zh-CN': zh,
    'en-US': en,
};

export function formatMessage(template: string, values?: Record<string, string | number>) {
    let text = template;
    for (const [key, value] of Object.entries(values ?? {})) {
        text = text.replaceAll(`{${key}}`, String(value));
    }
    return text;
}

export function translate(language: EditorLanguage, key: I18nKey, values?: Record<string, string | number>) {
    return formatMessage(editorMessages[language][key], values);
}

export function useI18n() {
    const language = useEditorStore((state) => state.language);
    return useCallback(
        (key: I18nKey, values?: Record<string, string | number>) => translate(language, key, values),
        [language],
    );
}
